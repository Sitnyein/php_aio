<?php
// define variables and set to empty values
$name = $email = $gender = $comment = $website = "";

if ($_SERVER["REQUEST_METHOD"] == "POST") {
  $name = test_input($_POST["name"]);
  $email = test_input($_POST["email"]);
  $website = test_input($_POST["website"]);
  $comment = test_input($_POST["comment"]);
  $gender = test_input($_POST["gender"]);
}

// trim($data): Removes leading and trailing whitespace from the input data.

// stripslashes($data): Removes backslashes from the input data. This is commonly used to prevent SQL injection, but it's less relevant for form data validation.

// htmlspecialchars($data): Converts special HTML characters (like <, >, &, etc.) to their respective HTML entities. This is used to prevent HTML injection and is essential when displaying user input on a web page.


   
function test_input($data) {
  $data = trim($data);
  $data = stripslashes($data);
  $data = htmlspecialchars($data);
  return $data;
}
?>

